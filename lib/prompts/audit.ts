export function buildAuditUserMessage(posts: { title: string; pubDate: string; excerpt: string }[]): string {
  const list = posts
    .map((p, i) => `${i + 1}. 제목: ${p.title}\n   발행일: ${p.pubDate}\n   본문 요약: ${p.excerpt}`)
    .join("\n");
  return `아래 게시물 목록을 감사하십시오. title은 반드시 원본 제목을 그대로 사용하십시오.\n\n${list}`;
}
